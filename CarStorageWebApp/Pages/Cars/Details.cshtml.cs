using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using CarStorageWebApp.Data;
using CarStorageWebApp.Models;

namespace CarStorageWebApp.Pages.Cars
{
    public class DetailsModel : PageModel
    {
        private readonly CarStorageWebApp.Data.CarStorageWebAppContext _context;

        public DetailsModel(CarStorageWebApp.Data.CarStorageWebAppContext context)
        {
            _context = context;
        }

      public Car Car { get; set; } = default!; 

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null || _context.Car == null)
            {
                return NotFound();
            }

            var car = await _context.Car.FirstOrDefaultAsync(m => m.ID == id);
            if (car == null)
            {
                return NotFound();
            }
            else 
            {
                Car = car;
            }
            return Page();
        }
    }
}
